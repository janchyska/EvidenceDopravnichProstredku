package prostredky;

/**
 *
 * @author janch
 */
public enum RidicskeOpravneni {
    AM("AM"),
    A1("A1"),
    A2("A2"),
    A("A"),
    NEZNAME("Neznámé");

    private final String ridicskeOpravneni;

    private RidicskeOpravneni(String ridicskeOpravneni) {
        this.ridicskeOpravneni = ridicskeOpravneni;
    }

    public String ridicskeOpravneni() {
        return this.ridicskeOpravneni;
    }

    public String getRidicskeOpravneni() {
        return this.ridicskeOpravneni;
    }

    @Override
    public String toString() {
        return this.ridicskeOpravneni;
    }

}
